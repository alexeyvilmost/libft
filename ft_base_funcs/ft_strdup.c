#include "libft.h"

char	*ft_strdup(const char *src)
{
	char	*new;
	size_t	size;

	if (!src)
		return (ft_strnew(0));
	size = ft_strlen(src);
	if (!(new = ft_strnew(size)))
		return (NULL);
	ft_memcpy(new, src, size);
	return (new);
}
