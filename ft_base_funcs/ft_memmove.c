#include "libft.h"

void	*ft_memmove(void *dest, const void *src, size_t size)
{
	unsigned char	*to_put;
	unsigned char	*to_copy;

	to_put = (unsigned char *)dest;
	to_copy = (unsigned char *)src;
	if (dest < src)
		return (ft_memcpy(dest, src, size));
	while (size--)
		to_put[size] = to_copy[size];
	return (dest);
}
