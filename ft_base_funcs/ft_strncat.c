#include "libft.h"

char	*ft_strncat(char *dest, const char *add, size_t size)
{
	size_t	i;

	i = ft_strlen(dest);
	while (*add && size--)
		dest[i++] = *add++;
	dest[i] = '\0';
	return (dest);
}
