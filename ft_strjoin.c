#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*new;

	if (!s1 || !s2 ||
			!(new = ft_strnew(ft_strlen((s1) + ft_strlen(s2)))))
		return (NULL);
	ft_strcat(ft_strcat(new, s1), s2);
	new[ft_strlen(new)] = '\0';
	return (new);
}
